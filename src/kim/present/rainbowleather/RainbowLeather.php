declare(strict_types=1);

namespace kim\present\rainbowleather;

use kim\present\rainbowleather\task\RainbowTask;
use pocketmine\plugin\PluginBase;

class RainbowLeather extends PluginBase{
	/** @var RainbowLeather */
	private static $instance;

	/** @return RainbowLeather */
	public static function getInstance() : RainbowLeather{
		return self::$instance;
	}

	/**
	 * Called when the plugin is loaded, before calling onEnable()
	 */
	public function onLoad() : void{
		self::$instance = $this;
	}

	/**
	 * Called when the plugin is enabled
	 */
	public function onEnable() : void{
		$this->getScheduler()->scheduleRepeatingTask(new RainbowTask(), 5);
	}
}